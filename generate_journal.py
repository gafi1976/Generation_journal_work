# ─── Генерация рабочих дней и сборка файла ───────────────────
blocks = []
current = date(2024, 7, 1)
end     = date(2026, 6, 19)

pool = RECORDS.copy()

# Перемешиваем пул заранее — равномерное распределение
random.shuffle(pool)
# Создаём очередь: каждый шаблон входит примерно одинаковое количество раз
# Повторяем пул столько раз, чтобы хватило на весь период, затем перемешиваем
import math
workdays_count = sum(1 for d in (date(2024,7,1) + timedelta(n)
                                  for n in range((date(2026,6,19)-date(2024,7,1)).days+1))
                     if is_workday(d))
repeats = math.ceil(workdays_count / len(pool)) + 1
shuffled_queue = pool * repeats
random.shuffle(shuffled_queue)

prev_rec  = None   # последний выбранный шаблон целиком
prev_type = None   # последний тип

while current <= end:
    if is_workday(current):
        if current in MAIPU_DAYS:
            rt, cat, ev, nt, exc = MAIPU_DAYS[current]
            blocks.append(make_record(current, (rt, cat, ev, nt), executor=exc))
            prev_type = rt
            prev_rec  = None
        else:
            # Кандидаты: другой тип И другой текст Events (не повторяем подряд)
            candidates = [r for r in shuffled_queue
                          if r[0] != prev_type and r != prev_rec]
            if not candidates:
                candidates = [r for r in shuffled_queue if r[0] != prev_type]
            if not candidates:
                candidates = shuffled_queue
            chosen    = candidates[0]
            shuffled_queue.remove(chosen)
            prev_type = chosen[0]
            prev_rec  = chosen
            # Чередуем исполнителей: чётные дни — exec1, нечётные — exec2
            exc = EXECUTOR if len(blocks) % 2 == 0 else (EXECUTOR_TWO or EXECUTOR)
            blocks.append(make_record(current, chosen, executor=exc))
    current += timedelta(days=1)

print(f'Сгенерировано записей: {len(blocks)}')

# ─── Сборка итогового файла ───────────────────────────────────
SEP = CRLF + '\x0c' + CRLF   # \r\n\x0c\r\n — точно как в оригинале

