def is_workday(d: date) -> bool:
    """Рабочий день: пн-пт и не праздник."""
    return d.weekday() < 5 and d not in HOLIDAYS


def make_record(d: date, rec_tuple: tuple) -> str:
    """Собирает текст одной записи для Lotus Notes."""
    record_type, category, events, note = rec_tuple
    date_str = d.strftime('%d.%m.%Y')
    lines = [
        't:  ',
        f'c:  {record_type}',
        f'date:  {date_str}',
        f'record_type:  {record_type}',
        f'record_type_r:  {record_type}',
        f'Executor:  {EXECUTOR}',
        'mail_node_name:  ',
        'mail_node_name_r:  ',
        'node_number:  ',
        'node_param:  ',
        'abonent_name:  ',
        'res_data:  ',
        'model_hardware:  ',
        'inventar:  ',
        'adv_param_name2:  ',
        'inventar_1:  ',
        'port:  ',
        'time_no_work:  ',
        'adv_param_name_3:  ',
        f'category:  {category}',
        'node_name:  ',
        'node_location:  ',
        'adv_param_label:  ',
        'adv_param_name:  ',
        'adv_param_label_2:  ',
        'adv_param_name2_r:  ',
        'adv_param_label_3:  ',
        'adv_param_name_3_r:  ',
        f'$UpdatedBy:  {UPDATED_BY}',
        '',
        f'Events:  {events}',
    ]
    if note:
        lines.append(f'Note:  {note}')
    lines.append('')
    return CRLF.join(lines)


# ─── Генерация рабочих дней и сборка файла ───────────────────
blocks = []
current = date(2024, 7, 1)
end     = date(2026, 6, 19)

# Перемешиваем банк чтобы подряд не повторялись одинаковые
pool = RECORDS.copy()

prev_type = None
while current <= end:
    if is_workday(current):
        # Выбираем случайно, но не повторяем тип два дня подряд
        candidates = [r for r in pool if r[0] != prev_type]
        if not candidates:
            candidates = pool
        chosen = random.choice(candidates)
        prev_type = chosen[0]
        blocks.append(make_record(current, chosen))
    current += timedelta(days=1)

print(f'Сгенерировано записей: {len(blocks)}')

# ─── Сборка итогового файла ───────────────────────────────────
SEP = CRLF + '\x0c' + CRLF   # \r\n\x0c\r\n — точно как в оригинале

final_text = SEP.join(blocks) + CRLF + '\x0c' + CRLF

with open(OUTPUT_FILE, 'wb') as f:
    f.write(final_text.encode(ENCODING))

print(f'Файл сохранён: {OUTPUT_FILE}  ({len(final_text.encode(ENCODING))} байт)')
