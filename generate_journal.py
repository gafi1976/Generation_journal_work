def is_workday(d: date) -> bool:
    """Рабочий день: пн-пт и не праздник."""
    return d.weekday() < 5 and d not in HOLIDAYS


def make_record(d: date, rec_tuple: tuple, executor: str = None) -> str:
    """Собирает текст одной записи для Lotus Notes."""
    record_type, category, events, note = rec_tuple
    exc = executor if executor else EXECUTOR
    date_str = d.strftime('%d.%m.%Y')
    lines = [
        't:  ',
        f'c:  {record_type}',
        f'date:  {date_str}',
        f'record_type:  {record_type}',
        f'record_type_r:  {record_type}',
        f'Executor:  {exc}',
        'mail_node_name:  ',
        'mail_node_name_r:  ',
        'node_number:  ',
        'node_param:  ',
        'abonent_name:  ',
        'res_data:  ',
        'model_hardware:  ',
        'inventar:  ',
        'adv_param_name2:  ',
        'inventar_1:  ',
        'port:  ',
        'time_no_work:  ',
        'adv_param_name_3:  ',
        f'category:  {category}',
        'node_name:  ',
        'node_location:  ',
        'adv_param_label:  ',
        'adv_param_name:  ',
        'adv_param_label_2:  ',
        'adv_param_name2_r:  ',
        'adv_param_label_3:  ',
        'adv_param_name_3_r:  ',
        f'$UpdatedBy:  {UPDATED_BY}',
        '',
        f'Events:  {events}',
    ]
    if note:
        lines.append(f'Note:  {note}')
    lines.append('')
    return CRLF.join(lines)


